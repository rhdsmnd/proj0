package enigma;

/** Class that represents a rotor that has no ratchet and does not advance.
 *  @author Ron Desmond
 */
class FixedRotor extends Rotor {

    /** initialize Rotor with string NAME,
     *which holds the information for FixedRotor. */
    public FixedRotor(String name) {
        super(name);
    }

    @Override
    boolean atNotch() {
        return false;
    }

    /** Fixed rotors do not advance. */
    @Override
    void advance() {
    }
}
